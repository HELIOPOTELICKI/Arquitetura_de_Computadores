; I/O Address Bus decode - every device gets 0x200 addresses */

IO0  EQU  0000h
IO1  EQU  0200h
IO2  EQU  0400h
IO3  EQU  0600h
IO4  EQU  0800h
IO5  EQU  0A00h
IO6  EQU  0C00h
IO7  EQU  0E00h
IO8  EQU  1000h
IO9  EQU  1200h
IO10 EQU  1400h
IO11 EQU  1600h
IO12 EQU  1800h
IO13 EQU  1A00h
IO14 EQU  1C00h
IO15 EQU  1E00h

;MEU CODIGO
CODE  SEGMENT 	  
	  org 0000h
;RESERVADO PARA VETOR DE INTERRUPCOES
	  org 0400h
;MEU CODIGO
inicio:
	MOV AX,DATA
	MOV DS,AX   ; DS AGORA APONTA PARA DATA SEGMENT
	MOV AX,EXTRA
	MOV ES,AX   ; ES AGORA APONTA PARA EXTRA SEGMENT
	MOV AX,STACK
	MOV SS,AX   ; SS AGORA APONTA PARA STACK SEGMENT

ESCRITA:
	LEITURA: 
	MOV DX, IO1
	IN AL, DX
	
	CMP AL, 00001100B
	JE ACENDE
	CMP AL, 00001101B
	JE ACENDE
	CMP AL, 00001111B
	JE ACENDE
	MOV AL, 00000000B
	MOV DX, IO0
	OUT DX, AL
	JMP LEITURA
	
		
	
	ACENDE:
	MOV DX, IO0
	
	MOV AL, 10000000B
	OUT DX, AL
	
	MOV AL, 01000000B
	OUT DX, AL
	
	MOV AL, 00100000B
	OUT DX, AL
	
	MOV AL, 00010000B
	OUT DX, AL
	
	MOV AL, 00001000B
	OUT DX, AL
	
	MOV AL, 00000100B
	OUT DX, AL
	
	MOV AL, 00000010B
	OUT DX, AL
	
	MOV AL, 00000001B
	OUT DX, AL
	
	MOV AL, 00000010B
	OUT DX, AL
	
	MOV AL, 00000100B
	OUT DX, AL
	
	MOV AL, 00001000B
	OUT DX, AL
	
	MOV AL, 00010000B
	OUT DX, AL
	
	MOV AL, 00100000B
	OUT DX, AL
	
	MOV AL, 01000000B
	OUT DX, AL
	
	MOV AL, 10000000B
	OUT DX, AL
	JMP LEITURA
	
	
JMP ESCRITA

CODE ENDS

;MILHA PILHA
STACK SEGMENT STACK      
DW 128 DUP(?) 
STACK ENDS 

;MEUS DADOS
DATA      SEGMENT  
DATA      ENDS

;EXTRA

EXTRA SEGMENT
EXTRA ENDS

end inicio